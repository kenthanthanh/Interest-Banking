    private void btnKetQuaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnKetQuaActionPerformed
        int idSP = this.sanPhamComboBox.getSelectedIndex();
        int idKiHan = this.kiHanComboBox.getSelectedIndex();
        // SanPham sanPham = SanPham.getSanPhamByID(idSP);
        if (idSP == 0 || idKiHan == 0 || (!bt_LaiSuat.isSelected() && !bt_TongSo.isSelected())) {
            JOptionPane.showMessageDialog(this,
                    "Vui lòng nhập đủ các trường yêu cầu!");
        } else {
            if (idSP == 1) {
                long soTienDauTu = Long.parseLong(this.soTienDauTu.getText());
                long laiSuatThuDuoc = laiSuatKiHan();
                long tongSoThuDuoc = laiSuatKiHan() + soTienDauTu;
                String tongSo = currencyVN.format(tongSoThuDuoc);
                String laiSuat = currencyVN.format(laiSuatThuDuoc);
                if (bt_TongSo.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Tổng số thu được: " + tongSo);
                } else if (bt_LaiSuat.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Lãi suất thu được: " + laiSuat);

                }

            } else if (idSP == 2) {
                long soTienDauTu = Long.parseLong(this.soTienDauTu.getText());
                long laiSuatThuDuoc = laiKepKiHan();
                long tongSoThuDuoc = laiKepKiHan() + soTienDauTu;
                String tongSo = currencyVN.format(tongSoThuDuoc);
                String laiSuat = currencyVN.format(laiSuatThuDuoc);
                if (bt_TongSo.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Tổng số thu được: " + tongSo);
                } else if (bt_LaiSuat.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Lãi suất thu được: " + laiSuat);

                }
            }
        }

        // TODO add your handling code here:
    }// GEN-LAST:event_btnKetQuaActionPerformed

