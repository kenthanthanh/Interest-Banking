    public long laiSuatKiHan() {
        long soTienDauTu = Long.parseLong(this.soTienDauTu.getText());
        float kiHanDauTu = chuyenKiHanDauTu();
        float laiSuatDauTu = Float.parseFloat(this.laiSuatDauTu.getText());
        long laiSuatDuThu = model.laiKiHan(soTienDauTu, kiHanDauTu, laiSuatDauTu);
        return laiSuatDuThu;
    }

    public long laiKepKiHan() {
        long soTienDauTu = Long.parseLong(this.soTienDauTu.getText());
        float kiHanDauTu = chuyenKiHanDauTu();
        float laiSuatDauTu = Float.parseFloat(this.laiSuatDauTu.getText());
        int chuKiLaiKep = chuyenDoiChuKi();
        long laiSuatDuThu = (long) model.laiKepKiHan(soTienDauTu, kiHanDauTu, laiSuatDauTu, chuKiLaiKep);
        return laiSuatDuThu;
    }

    public float chuyenKiHanDauTu() {
        int luaChonKiHan = this.kiHanComboBox.getSelectedIndex();
        float kiHanDauTu = Float.parseFloat(this.kyHanDauTu.getText());
        if (luaChonKiHan == 1) {
            // kì hạn đầu tư theo tháng -> bằng chính nó
            kiHanDauTu *= 1;
        } else if (luaChonKiHan == 2) {
            // kì hạn đầu tư theo năm -> số năm *12 = số tháng
            kiHanDauTu *= 12;
        }
        // else {
        // JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ dữ liệu");
        // }
        return kiHanDauTu;
    }

    public int chuyenDoiChuKi() {
        int luaChonChuKi = this.chuKiComboBox.getSelectedIndex();
        int chuKiLaiKep = 1;
        if (luaChonChuKi == 1) {
            chuKiLaiKep *= 365;
        } else if (luaChonChuKi == 2) {
            chuKiLaiKep *= 12;
        } else if (luaChonChuKi == 3) {
            chuKiLaiKep *= 4;
        } else if (luaChonChuKi == 4) {
            chuKiLaiKep *= 1;
        }
        // } else {
        // JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ dữ liệu");
        // }
        return chuKiLaiKep;
    }

